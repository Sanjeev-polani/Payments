package Day1;

public class Datavariables {

	public static void main(String[] args) 
	{
		int a;  	//declaration
		a=100;		//assignment
		
		System.out.println(a);
		
		a =200;
		System.out.println(a);
		
		
		//Approach 1
		int e =500;
		int b =600;
		int c =700;
		
		//Approach 2
		int h,i; 		//declaration
		
		
		char grade = 'A';
		System .out.println(grade);
		byte by =125;
		System .out.println(by);
		
		short sh = 3535;
		System .out.println(sh);
		
		long   l = 123456789L;  //literal is needed L
		System .out.println(l);
		
		float item =15.5F;  //literal is needed F
		System .out.println(item);
		
		double dbl = 1234.489938938;
		System .out.println(dbl);
	}

}
